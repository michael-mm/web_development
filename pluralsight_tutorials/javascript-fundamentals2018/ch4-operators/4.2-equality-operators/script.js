// Equality Operators

console.log(1 == 1); // true

console.log(1 == '1'); // true 
// javascript attempts to convert the types to be the same

// Strict Equality Operators

console.log(1 === 1); // true

console.log(1 === '1'); // false 
// no type conversion, type & values MUST be true for execution 



// Inequality Operators

console.log(1 != 1); // false
console.log(1 != '1'); // false

// Strict Inequality Operators

console.log(1 !== 1); // false
console.log(1 !== '1'); // true
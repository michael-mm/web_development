var myValue = 6;

function testValue()
{
  if (myValue == 5)
  {
    console.log("The condition was met");
  }
  else if (myValue == 6)
  {
    console.log("The else if condition was met");
  }
  else
  {
    console.log("The condition was NOT met");
  }
  //document.write("The function was executed");
}

testValue();
